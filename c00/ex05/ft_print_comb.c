#include <unistd.h>

void	ft_print_comb(void)
{
	char	n[3];

	n[0] = 48;
	while (n[0] <= 57)
	{
		n[1] = 48;
		while (n[1] <= 57)
		{
			n[2] = 48;
			while (n[2] <= 57)
			{
				if (n[0] < n[1] && n[1] < n[2])
				{
					if (! (n[0] == 48 && n[1] == 49 && n[2] == 50))
					{
						write(1, ", ", 2);
					}
					write(1, &n, 3);
				}
				n[2]++;
			}
		n[1]++;
		}
	n[0]++;
	}
}
