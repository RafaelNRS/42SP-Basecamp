int	ft_iterative_factorial(int nb)
{
	int	i;
	int	result;

	if (nb < 0)
		return (0);
	i = 1;
	result = 1;
	while (nb >= i)
	{
		result = result * nb;
		nb--;
	}
	return (result);
}
